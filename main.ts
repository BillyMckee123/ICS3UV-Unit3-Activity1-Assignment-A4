// Assignment A4 - Average of Three Numbers
// Calculates the average of 56.9, 89.7, 90.2

const num1: number = 56.9;
const num2: number = 89.7;
const num3: number = 90.2;

const sum: number = num1 + num2 + num3;
const average: number = sum / 3;

console.log(`The average of ${num1}, ${num2}, and ${num3} is ${average}.`);
